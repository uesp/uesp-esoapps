			local name = GetAbilityName(abilityId)
			local buffer = tostring(name).." ("..tostring(abilityId)..") - Duration"
			diffSkills = diffSkills + 1
			data[#data+1] = buffer
			uespLog.Msg(".    "..buffer)
		end
		
